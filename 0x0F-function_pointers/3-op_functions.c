/**
 * op_add - adds 2 numbers
 *
 * @a: first
 *
 * @b: second
 *
 * Return: sum
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts 2 numbers
 *
 * @a: first
 *
 * @b: second
 *
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies 2 numbers
 *
 * @a: first
 *
 * @b: second
 *
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two numbers
 *
 * @a: first
 *
 * @b: second
 *
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - divides a by b
 *
 * @a: first number
 *
 * @b: second number
 *
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}


